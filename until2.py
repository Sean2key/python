# 字符串首字母大写
name = "alex bobby"
print(name.title())
# 字符串全部大写
print(name.upper())
# 字符串全部小写
print(name.lower())

# 字符串删除空白
language = '  python  '
# 删除右边
print(language.rstrip())
# 删除左边
print(language.lstrip())
# 删除全部
print(language.strip())

# 引号的利用
article = 'This is a "story"'
print(article)
article = "This is a 'story'"
print(article)

# 数字运算
print(2*3)			# 乘
print(2+3)			#加
print(2-3)			#减
print(6/3)			#除
print(2**3)			#次方
# 数字转字符串 str()
age = 27
print("My age is " + str(age))

